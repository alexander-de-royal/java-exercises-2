package src.looping;

public class ForTest {
    public static void main(String[] args) {
        /*
        * --------------- for loop ----------------
        *
        * Syntax:
        *
        * for (initialization; condition; inc/dec)
        *   {
        *       //statements
        *   }
        * */

        for (int i = 1; i <= 50; i++){
            System.out.println("Hello " + i);
        }
    }
}
