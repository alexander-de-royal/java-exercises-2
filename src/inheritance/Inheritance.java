package src.inheritance;

public class Inheritance {

    /*
    * ------------ Inheritance ---------------
    *  # use for code re-usability
    *  # process of accessing properties and methods of parent/super/base class to child/sub/derived class is known as
    *   inheritance.
    *
    *  # Also called IS -a relationship
    *  # It's a Generaliztaion to Specializtion process
    *  # we can inherit  only one class at a time
    *  # we can't inherit private properties, methods and constructors.
    *  # 'extends' keyword is used for inheritance.
    *  # we can inherit static properties and methods
    *
    *  Syntax:
    *   child-class         extends        parent-class {
    *       // properties : child + parent
    *
    *       // methods: child + parent
    *   }
    * */
}
