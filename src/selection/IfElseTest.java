package src.selection;

public class IfElseTest {
    public static void main(String[] args) {

        /*
        *  --------------- if-else ---------------
        *
        * Syntax
        *
        *   if (condition) {
        *       // statements
        *   } else {
        *       // statements
        *   }
        * */

        int a = 343;
        int b = 344;

        if (a > b) {
            System.out.println(a);
        } else {
            System.out.println(b);
        }
    }
}
