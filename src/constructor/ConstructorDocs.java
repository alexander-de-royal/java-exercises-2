package src.constructor;

public class ConstructorDocs {
    public static void main(String[] args) {

        /*
        *  --------------------- Constructor --------------------------
        *  # used to create an object in Java
        *  # special type of method which has same name as class name
        *  # if there is no any user defined constructor in class then java provides default constructor to create
        *   object.
        *
        *  # Types:
        *   1) Default Constructor:
        *       - used to create object and set default values to it
        *           Syntax:
        *               class_name(){
        *                   // set default values to object
        *               }
        *   2) Parameterized Constructor:
        *       - used to create object and set dynamic values to it.
        *           Syntax:
        *               class_name(arg1, arg2,....,argn){
        *                   // set dynamic values to object
        *               }
        * */
    }
}
